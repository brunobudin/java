public class Continhas{
    public static void main(String args[]){
        int a,b,c;

        a = 10;
        b = 3;

        c = a + b;
        System.out.println("Valor da soma = " + c);

        c = a - b;
        System.out.println("Valor da subtração = " + c);

        c = a * b;
        System.out.println("Valor da Multiplicação = " + c);


    }
       
}